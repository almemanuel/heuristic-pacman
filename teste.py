# Função para inverter o tabuleiro
def inverter_tabuleiro(tabuleiro):
    # Inicializar um novo tabuleiro invertido
    tabuleiro_invertido = []
    for linha in reversed(tabuleiro):
        # Inverter cada linha
        linha_invertida = list(reversed(linha))
        tabuleiro_invertido.append(linha_invertida)
    return tabuleiro_invertido

